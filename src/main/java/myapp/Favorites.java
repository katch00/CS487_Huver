package myapp;

public class Favorites {

    String username;
    String address;
    String city;
    String zipCode;
    String state;

    public Favorites(String username, String address, String city, String zipCode, String state)
    {
        this.username = username;
        this.address = address;
        this.city = city;
        this.zipCode = zipCode;
        this.state = state;
    }

}